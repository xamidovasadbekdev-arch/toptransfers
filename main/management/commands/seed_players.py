class Command(BaseCommand):
    help = 'Seed database with real players for all clubs'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Clear existing players before seeding',
        )

    def handle(self, *args, **options):
        if options['clear']:
            self.stdout.write('Clearing existing players...')
            Player.objects.all().delete()
            self.stdout.write(self.style.WARNING('All players deleted.'))

        total_created = 0
        total_skipped = 0
        not_found_clubs = []

        for club_name, players in PLAYERS_DATA.items():
            try:
                club = Club.objects.get(name=club_name)
            except Club.DoesNotExist:
                self.stdout.write(self.style.WARNING(f'⚠️  Club not found: {club_name}'))
                not_found_clubs.append(club_name)
                continue

            self.stdout.write(f'\n⚽ Processing {club_name}...')

            for p in players:
                name, age, position, number, price, nationality = p

                if Player.objects.filter(name=name, club=club).exists():
                    self.stdout.write(f'  ⏩ Skipped: {name}')
                    total_skipped += 1
                    continue

                country, _ = Country.objects.get_or_create(name=nationality)

                Player.objects.create(
                    name=name,
                    age=age,
                    position=position,
                    number=number,
                    price=price,
                    country=country,
                    club=club,
                )
                self.stdout.write(self.style.SUCCESS(f'  ✅ {name}'))
                total_created += 1

        self.stdout.write('\n' + '=' * 50)
        self.stdout.write(self.style.SUCCESS(f'✅ Done! Created: {total_created} players'))
        if total_skipped:
            self.stdout.write(self.style.WARNING(f'⏩ Skipped: {total_skipped} (already existed)'))
        if not_found_clubs:
            self.stdout.write(self.style.ERROR(f'❌ Clubs not found: {", ".join(not_found_clubs)}'))
